import java.lang.*;
class Variables
{
public static void main(String arg[])
{
int i=189;
System.out.println(i);

byte b=54;
System.out.println(b);

short s=44;
System.out.println(s);

float f=23.7f;
System.out.println(f);

char c='A';
System.out.println(c);

int x=-5;
System.out.println(Integer.toBinaryString(x));

}
}