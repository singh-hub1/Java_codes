package Stringclass;

public class String1 {

	public static void main(String[] args) {
		int x=10,y=20;
		
		System.out.println(x+y +" Sum"); //30 Sum
		
		System.out.println("Sum "+ x+y);  //Sum 1020
		
		
		System.out.println("Sum of "+x+" and "+y+" is "+(x+y));  //Sum of 10 and 20 is 30
		
		System.out.printf("hello\n"); //hello
	}

}
